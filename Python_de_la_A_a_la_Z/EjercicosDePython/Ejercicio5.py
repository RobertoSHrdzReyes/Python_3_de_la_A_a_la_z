#Escribir un programa que pregunte al usuario por el 
# número de horas trabajadas y el coste por hora. 
# Después debe mostrar por pantalla la paga que le corresponde.
HorasTrabajadas = float(input("Cuantas horas trabajó?"))
CostoPorHora = float(input("Cuanto se paga por hora?"))
PagoCorrespondiente=(HorasTrabajadas)*(CostoPorHora)
print("Su pago es de: $",PagoCorrespondiente)


